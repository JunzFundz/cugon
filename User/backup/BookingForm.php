    <script>
        function validateFrom() {

            const numAdults = document.form.numAdults.value;
            const numChild = document.form.numChild.value;
            const start = document.form.start.value;
            const end = document.form.end.value;

            if (isNaN(numAdults) || isNaN(numChild)) {
                alert("Please enter a valid number for the number of adults and children.");
                return false;
            } else if ((Date.parse(start) > Date.parse(end))) {
                alert('Check in must be before check out');
                return false;
            }else if (start == null || start == '') {
                alert('Start date required')
                return false;
            } else if (end == null || end == '') {
                alert('End date is required')
                return false;
            }
        }
